/**
 * @file render.h
 * @brief Интерфейс функций отрисовки CLI для BrickGame.
 *
 * Содержит объявления функций для визуализации игрового процесса в терминале
 * с использованием ncurses: игрового поля, стартового экрана, экранов Game
 * Over, победы и ошибок загрузки. Также предоставляет меню выбора игры.
 *
 * Эти функции отвечают только за отображение информации и не содержат игровой
 * логики — данные поступают через структуру GameInfo_t.
 */

#ifndef BRICKGAME_TETRIS_RENDER_H_
#define BRICKGAME_TETRIS_RENDER_H_

#include "../../brickgame/common/types.h"
#include "app_controller.h"
/**
 * @brief Отрисовывает текущее состояние игры.
 *
 * Использует данные из структуры GameInfo_t для отображения поля, счёта,
 * уровня и прочей игровой информации.
 *
 * @param info Указатель на структуру с актуальным состоянием игры.
 */
void render_game(const GameInfo_t* info);

/**
 * @brief Отображает стартовый экран.
 *
 * Показывает приветственное сообщение и инструкции перед запуском игры.
 */
void renderStartScreen();

/**
 * @brief Отображает экран Game Over.
 *
 * Используется при проигрыше игрока.
 */
void renderGameOverScreen();

/**
 * @brief Отображает экран победы.
 *
 * Вызывается, если игрок выиграл игру.
 */
void renderGameWonScreen();

/**
 * @brief Отображает меню выбора игры.
 *
 * Предлагает игроку список доступных игр и возвращает выбранный вариант.
 *
 * @return GameType Тип выбранной игры.
 */
GameType render_game_selection();

/**
 * @brief Отображает сообщение об ошибке загрузки.
 *
 * Используется, если возникли проблемы с инициализацией игры.
 *
 * @param error Строка с текстом ошибки.
 */
void render_loading_error(const char* error);

#endif  // BRICKGAME_TETRIS_RENDER_H_